// demo of how to access list objects using their indexes

void main() {
  List ages = [22, 21, 22, 24, 20, 17]; 
  print(ages[0]);
  print(ages[1]);
  print(ages[2]);
  print(ages[4]);

  var info = ["Jack", 22, "Kumasi", 87.23];
  print('My name is ${info[0]} from ${info[2]}. I am ${info[1]} years old.');

}
